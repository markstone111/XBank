    private static final HashSet<String> usedNumbers = new HashSet<>();
    private static final Random random = new Random();

    public static String generateAccountNumber() {
        String accountNo;

        do {
            // Take last 10 digits of current time in milliseconds
            String timePart = String.valueOf(System.currentTimeMillis());
            if (timePart.length() > 10) {
                timePart = timePart.substring(timePart.length() - 10);
            }

            // Add 1 random digit (0â€“9) to make 11 digits
            int randomDigit = random.nextInt(10);

            accountNo = timePart + randomDigit;

        } while (usedNumbers.contains(accountNo)); // Ensure uniqueness within the session

        usedNumbers.add(accountNo);
        return accountNo;
    }
    

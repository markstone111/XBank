        String fullName = jTextField1.getText();
        String phoneNo = jTextField2.getText();
        String accountNo = generateAccountNumber();
        
        String aadharNo = jTextField3.getText();
        String panNo = jTextField4.getText();
        String pincode = jTextField5.getText();
        String state = jTextField6.getText();
        String city = jTextField7.getText();
        String adress = jTextField8.getText();
        
        System.out.println("Account No: " +accountNo);
        System.out.println("Phone No: " +phoneNo);
        System.out.println("Full Name: " +fullName);
        System.out.println("Aadhar No: " + aadharNo);
        System.out.println("PAN No: " + panNo);
        System.out.println("Pincode: " + pincode);
        System.out.println("State: " + state);
        System.out.println("City: " + city);
        System.out.println("Address: " + adress);
        
        if (fullName.isEmpty() || phoneNo.isEmpty() || accountNo.isEmpty()) {
            JOptionPane.showMessageDialog(NewCustomerRegistration.this, "Full Name, Phone No, and Aadhaar No are required.", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (accountNo.length() < 11 || !Pattern.matches("^[0-9]+$", accountNo)) {
            JOptionPane.showMessageDialog(NewCustomerRegistration.this, "Aadhaar No (Account No) must be at least 11 digits and contain only numbers.", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try{
            
            String sql = "INSERT INTO customer (account_no, customer_name, phone_no) VALUES ('"
                   + accountNo + "','"
                   + fullName + "','"
                   + phoneNo + "')";
            
            System.out.println(sql);
            String sql1 = "INSERT INTO kyc (account_no, aadhar_no, pan_no, phno, adress, pincode, state, city) VALUES ('"
                    + accountNo + "', '"
                    + aadharNo + "', '"
                    + panNo + "', '"
                    + phoneNo + "', '"
                    + adress + "', '"
                    + pincode + "', '"
                    + state + "', '"
                    + city + "')";
            System.out.println(sql1);
            
            int rowsAffected;
            rowsAffected = db.stmt.executeUpdate(sql);
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(NewCustomerRegistration.this,
                    "Account Created Successfully!\nAccount No: " + accountNo,
                    "Success",
                    JOptionPane.INFORMATION_MESSAGE);
                NewCustomerRegistration.this.dispose();
            } else {
                JOptionPane.showMessageDialog(NewCustomerRegistration.this, "Failed to create account.", "Database Error", JOptionPane.ERROR_MESSAGE);
            }
            
            rowsAffected = db.stmt.executeUpdate(sql1);
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(NewCustomerRegistration.this,
                    "Account KYC completed Successfully!\nAccount No: " + accountNo,
                    "Success",
                    JOptionPane.INFORMATION_MESSAGE);
                NewCustomerRegistration.this.dispose();
            } else {
                JOptionPane.showMessageDialog(NewCustomerRegistration.this, "Failed to complete the KYC. ", "Database Error", JOptionPane.ERROR_MESSAGE);
            }


        } catch (Exception e) {
            e.printStackTrace();
        }
       
        NewCustomerRegistration.this.dispose();
